def calculator():
    print(logo)

    def add(n1, n2):
        return n1 + n2

    def sub(n1, n2):
        return n1 - n2

    def multiply(n1, n2):
        return n1 * n2

    def divide(n1, n2):
        return n1 / n2

    operations = {
        "+": add,
        "-": sub,
        "*": multiply,
        "/": divide
    }

    while input("Do you want to start a new calculation?(Y/N): ") == "Y":
        num1 = float(input("Input the Number 1:\n"))
        for symbols in operations:
            print(symbols)
        answer = num1
        operation_symbol = input("Pick an operation from the symbols above\n")
        num2 = float(input("Input the next number:\n"))
        calculation_function = operations[operation_symbol]
        answer = calculation_function(answer, num2)
        print(f"{num1} {operation_symbol} {num2} = {answer}")

    print("Calculator Shut Down!!!!")


calculator()
